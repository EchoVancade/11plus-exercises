function classifyEngagement(text){
  const t=(text||'').trim().toLowerCase();
  if(!t)return 'disengaged';
  const tokens=t.split(/\s+/);
  if(/^(idk|dunno|dk|no\s*idea|don'?t\s*know|i\s*don'?t\s*know|i\s*dunno|nope|nah|na)$/.test(t))return 'disengaged';
  if(tokens.length===1 && /^(yes|no|ok|okay|sure|maybe|huh|what|um|hmm|err|uh|good|great|nice|cool|fine|yeah|yep)$/.test(t))return 'disengaged';
  return 'normal';
}

