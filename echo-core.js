function speakWebSpeech(text){
  try{if(window.speechSynthesis.paused)window.speechSynthesis.resume();}catch(e){}
  window.speechSynthesis.cancel();
  const myGen=++_speechGen;                         // generation stamp for this utterance
  setTimeout(()=>{
    if(myGen!==_speechGen)return;                   // hardStop() fired during 120ms — bail
    const u=new SpeechSynthesisUtterance(text);
    _currentUtt=u;
    if(_cachedVoice)u.voice=_cachedVoice;
    u.rate=CHAR.key==='snake'?.92:.98;
    u.pitch=CHAR.key==='snake'?.95:1.02;
    u.volume=1;
    u.onend=()=>{
      if(myGen!==_speechGen)return;                 // stale — another speak() took over
      _currentUtt=null;
      try{_vizStop();}catch(e){}
      try{const k=new SpeechSynthesisUtterance('\u00A0');k.volume=0.01;k.rate=16;window.speechSynthesis.speak(k);}catch(e){}
    };
    u.onerror=(ev)=>{
      if(myGen!==_speechGen)return;                 // cancelled by hardStop — NEVER retry
      if(ev?.error==='canceled'||ev?.error==='interrupted')return; // explicit cancel — ignore
      // genuine hardware error — fail silently, do not re-queue
    };
    window.speechSynthesis.speak(u);
    _fireOnset();
    if(_latestViz){const cv=_latestViz.querySelector('canvas');if(cv)_drawIdleViz(_latestViz,cv.getContext('2d'),cv.width,cv.height,_latestViz.querySelector('.viz-orb'),_latestViz.dataset.rgb);}
  },120);
}

